# Curriculum timing: {juku: {grade_month: [topics]}}
# grade_month format: "4-04" = 4年生4月, "5-09" = 5年生9月
# Each problem has: subject, topic, question, answer, difficulty, curriculum timing

problems = []

def add(subject, topic, q, a, diff='standard', freq='medium', 
        yotsuya=None, sapix=None, nichinoken=None, tags=None, ptype=None):
    """Add a problem with curriculum timing for each juku."""
    p = {
        'subject': subject,
        'topic': topic,
        'question': q,
        'answer': a,
        'difficulty': diff,
        'frequency': freq,
        'tags': tags or [],
    }
    if ptype:
        p['problemType'] = ptype
    timing = {}
    if yotsuya: timing['yotsuya'] = yotsuya
    if sapix: timing['sapix'] = sapix
    if nichinoken: timing['nichinoken'] = nichinoken
    if timing:
        p['curriculum'] = timing
    problems.append(p)

